import turtle as t
import time
t.color=('black','yellow','red','pink','blue')
t.pensize(1)
t.penup()
t.goto(50,80)
t.left(45)
t.pendown()
t.circle(30,230)
t.goto(0,100)
t.right(90)
t.circle(60,180)
t.right(100)
t.fd(100)
t.penup()
t.goto(50,80)
t.left(25)
t.pendown()
t.fd(150)
time.sleep(5)