t.fd(20)
t.circle(6,180)
t.fd(5)
t.left(180)
t.fd(10)
t.circle(7,180)
t.fd(10)
t.left(180)
t.fd(20)
t.circle(8,180)
t.fd(40)
t.circle(20,180)
t.fd(3)
t.end_fill()
